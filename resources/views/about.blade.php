            <h2>Le troc & la loi</h2>
            <section>
            <p>Il existe une loi qui encadre le troc en France : la loi du 17 mars 1804. Selon cette législation, ce type d’échange génère un contrat tacite entre les deux partis. Seul le consentement mutuel des co-permutants (les deux personnes désireuses d’effectuer un troc) est nécessaire à la transaction. En cas d’échange de main à main, il est toujours possible de refuser l’échange si le produit ne satisfait pas le receveur. En cas d’échange par voie postale, si l’objet reçu n'est pas conforme à la description faite par le donneur, il est possible de demander une annulation. Pour cela, une preuve, à savoir un contrat écrit, peut s’avérer nécessaire. En revanche, si l’objet est conforme mais abîmé pendant le transport, alors aucune annulation ne sera possible.</p>

<p>La transaction n’engage que les deux partenaires d’échange. <b>Faistroquer.fr</b>, au même titre que toutes les plateformes d’échanges, décline toute responsabilité en cas de problème. Celle-ci incombe au co-permutant fautif.</p>

<p>La législation interdit formellement les échanges d’alcool, cigarette, médicaments et tous produits illicites. Par ailleurs, les services à caractère sexuel ou toute autre activité illégale selon le code pénal sont naturellement prohibés. Toute annonce répréhensible par la loi sera supprimée et fera l’objet d’un signalement auprès des autorités compétentes.</p>

