            <p>Si vous recherchez un prestataire de services près de chez vous, qui ne vous demandera pas d’argent en échange mais seulement vos propres compétences ou un peu de votre temps, vous êtes au bon endroit ! Trouvez rapidement un baby-sitter, un mécanicien, une personne qui a la main verte pour s’occuper de votre jardin… On vous a bien dit « gratuit », alors nous tiendrons notre promesse jusqu’au bout. Sur Faistroquer.fr, aucune commission ne vous sera demandée, du début jusqu’à la fin de l’échange.</p>
        </div>

        <p>Vous pouvez d’ores et déjà poster vos annonces ou en consulter ici. Trouvez la bonne rubrique et lancez-vous ! Si toutefois vous préférez échanger vos services contre des biens, c’est possible aussi. Nous avons conçu un site de troc généraliste consacré aux échanges d’objets, de savoirs, de services, et même au prêt, au don, à l’adoption et aux activités de groupe !</p>

        <div class="subheading">Quelques idées d’échanges de services</div>

        <p>Les idées sont légion en termes de possibilités de troc de services. Voici quelques tâches pour lesquelles on peut demander de l’aide, ou que l’on peut, à l’inverse, proposer soi-même en échange d’un service rendu.</p>

        <div class="subheading">La cuisine</div>

        <p>Certains d’entre nous avons appris des recettes de gâteaux dès l’enfance et apprécions toujours de mitonner des petits plats une fois arrivés à l’âge adulte. D’autres jurent qu’ils ne sauraient faire cuire un œuf sans transformer la cuisine en champ de bataille. Ça tombe bien : offrir un plat cuisiné est un geste communément apprécié. Alors, proposez vos talents de cuisinier(e) en échange d’un service ! Bien entendu, les co-échangeurs peuvent être tous deux d’excellents cordons bleus. Auquel cas, un échange de plats est l’occasion de varier un peu les plaisirs en faisant goûter à ses propres spécialités.</p>

        <div class="subheading">La cueillette</div>

        <p>Si la condition physique du troqueur le permet, il s’agit d’un service très simple à rendre, qui ne nécessite pas de compétences particulières. Pour autant, une personne âgée sera très reconnaissante envers une un peu plus jeune qui aura transféré les poires de son jardin vers ses paniers à fruits. Idem pour un homme ou une femme accaparé(e) par son travail qui n’a guère eu le temps de s’en occuper. Une idée d’échange de service gratuit facile à mettre en œuvre.</p>

        <div class="subheading">Le montage de meubles</div>

        <p>Certains d’entre nous sommes manuels, d’autres moins. Pour ceux qui se sentent habiles de leurs mains, aider au montage ou à la réparation d’une étagère est source de soulagement pour ceux qui redoutent de se donner accidentellement un coup de marteau sur les doigts… Ou alors, si on le souhaite, c’est aussi l’occasion de dépasser ses croyances envers soi-même et d’apprendre par observation de l’autre !</p>

        <div class="subheading">La mécanique</div>

        <p>Cette discipline peut concerner aussi bien l’entretien de la voiture que les réparations de vélo. Tout le monde n’a pas eu l’occasion d’apprendre à s’en occuper. Comme pour le montage de meubles, il ne s’agit pas d’un savoir inné, mais regarder l’autre à l’œuvre, c’est l’occasion d’apprendre ! Acheter un scooter, c’est s’assurer liberté et praticité. Connaître quelques rudiments concernant l’entretien, c’est encore mieux. Mais pour les plus réfractaires, l’échange de services permet aussi de se soustraire définitivement à la tâche !</p>

        <div class="subheading">Autres idées en vrac</div>

